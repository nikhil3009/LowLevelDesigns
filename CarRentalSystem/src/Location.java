public class Location {
    int locId;
    String address;
    int zipcode;
    String city;
    String State;

    public Location(int locId, String address, int zipcode, String city, String state) {
        this.locId = locId;
        this.address = address;
        this.zipcode = zipcode;
        this.city = city;
        State = state;
    }
}
