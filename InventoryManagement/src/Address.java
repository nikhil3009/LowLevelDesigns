public class Address {
    int zipcpde;
    String city;
    String state;

    public int getZipcpde() {
        return zipcpde;
    }

    public void setZipcpde(int zipcpde) {
        this.zipcpde = zipcpde;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }
}
